package com.thebluealliance.models;

public class DistrictEntry {
	
	private String abbreviation, display_name, key;
	private int year;
	
	public String getAbbreviation() {
		return abbreviation;
	}
	public String getDisplayName() {
		return display_name;
	}
	public String getKey() {
		return key;
	}
	public int getYear() {
		return year;
	}
	

}
