package com.thebluealliance.models;

public class Robot {
	
	private String key, robot_name, team_key;
	private int year;
	
	public String getKey() {
		return key;
	}
	public String getRobotName() {
		return robot_name;
	}
	public String getTeamKey() {
		return team_key;
	}
	public int getYear() {
		return year;
	}
	

}
